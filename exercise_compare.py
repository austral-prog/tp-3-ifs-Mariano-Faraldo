def compare():
    numero_1 = int(input())

    numero_2 = int(input())

    if numero_1 > numero_2:
        print(f"{numero_1} es mayor que {numero_2}")
    else:
        if numero_1 < numero_2:
            print(f"{numero_1} es menor que {numero_2}")

        elif numero_1 == numero_2:
            print(f"{numero_1} es igual a {numero_2}")

